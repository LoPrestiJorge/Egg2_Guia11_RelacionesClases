package Entidades;

public class DNI {
    int numero; 

    public DNI() {
    }

    public DNI(int numero) {
        this.numero = numero;
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    @Override
    public String toString() {
        return "DNI{" + "numero=" + numero + '}';
    }
    
    


 }



